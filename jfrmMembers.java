    private void jtblMembersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblMembersMouseClicked
        int selectedRowIndex = this.jtblMembers.getSelectedRow();
        
        if(selectedRowIndex == -1){
            return;
        }
        
        this.memberID = (int) this.jtblMembers.getValueAt(selectedRowIndex, 0);
        this.jtxtFirstName.setText(this.jtblMembers.getValueAt(selectedRowIndex, 1).toString());
        this.jtxtMiddelName.setText(this.jtblMembers.getValueAt(selectedRowIndex, 2).toString());
        this.jtxtLastName.setText(this.jtblMembers.getValueAt(selectedRowIndex, 3).toString());
        this.jcbxGender.setSelectedItem(this.jtblMembers.getValueAt(selectedRowIndex, 4).toString());        
        this.jdtPSubscriptionDate.setDate(Date.valueOf(this.jtblMembers.getValueAt(selectedRowIndex, 5).toString()));
    }//GEN-LAST:event_jtblMembersMouseClicked

    
