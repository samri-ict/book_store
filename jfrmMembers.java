    private void jbtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSaveActionPerformed
        
        if("".equals(this.jtxtFirstName.getText()) ||
           ("".equals(this.jtxtMiddelName.getText()) &&
                "".equals(this.jtxtLastName.getText())))
        {
            JOptionPane.showMessageDialog(this, "Please Insert Member Name before you proceed.");
            return;
        }
        
        Members newMembers; 
        newMembers = new Members(this.memberID, this.jtxtFirstName.getText(),
                this.jtxtMiddelName.getText(),this.jtxtLastName.getText(),
                Gender.getValueOf(this.jcbxGender.getSelectedItem().toString()),
                this.jdtPSubscriptionDate.getDate());
        try {
            boolean recordSaved = _rmi.AddMembers(newMembers);
            if(recordSaved){ 
                    JOptionPane.showMessageDialog(this, "Record Saved Successfully");                    
                    this.jbtnSearchActionPerformed(evt);
            }
            else{
                 JOptionPane.showMessageDialog(this, "Sorry, Unable to save Record");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(jfrmMembers.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.jbtnClearFrameActionPerformed(evt);
    }//GEN-LAST:event_jbtnSaveActionPerformed

    private void jbtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSearchActionPerformed
        
        this.dtMembers.getDataVector().removeAllElements();
        ArrayList<Members> allMembers;
        Object[] ob;
        
        try {
            allMembers =  _rmi.fetchAllMembers();
            for (int i = 0; i < allMembers.size(); i++) {
                Members mbr = allMembers.get(i);
                
                ob=new Object[6];
                ob[0] = mbr.MemberId;
                ob[1] = mbr.FirstName;
                ob[2] = mbr.MiddelName;
                ob[3] = mbr.LastName;
                ob[4] = mbr.Gender;
                ob[5] = mbr.DateSubscribed;
                dtMembers.addRow(ob);
            }
            
            
        } catch (RemoteException ex) {
            Logger.getLogger(jfrmMembers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtnSearchActionPerformed

